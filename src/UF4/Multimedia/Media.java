public class Media {
    private String nom;
    private int durada;
    public String getNom() {
        return nom;
    }

    /**
     *
     * @param nom
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getDurada() {
        return durada;
    }

    /**
     *
     * @param durada
     */
    public void setDurada(int durada) {
        this.durada = durada;
    }

}
